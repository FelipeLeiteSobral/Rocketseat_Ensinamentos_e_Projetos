//  Operadores Aritméticos

// multiplicação (*)
console.log(5*3)

// divisão (/)
console.log(12/3)

// soma (+)
console.log(2+6)
 
//subtração (-)
console.log(6-2)

// resto da divisão (%)
let remainder
remainder = 11%9
console.log(remainder)

// incremento (++)
let increment = 0
console.log(++increment)
console.log(increment++)
console.log(increment)

// decremento (--)
let decrement = 5
console.log(--decrement)
console.log(decrement--)
console.log(decrement)

// exponencial (**)
console.log(3**3)